a = [1,4,9,6,66,190,10 ]
print(a)

# Sorting list 
a.sort()
print(a)

# reverse sorted order
a.reverse()
print(a)

# (append) add an element at the end of the list
a.append(45)
print(a)

# insert at given index 
a.insert(4, 99)
print(a)

# (pop) remove at given index 
a.pop(2)
print(a)

# remove from list 
a.remove(99)
print(a)